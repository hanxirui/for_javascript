/**
*error message show
*inputId 输入框ID
*spanId 显示错误信息ID
*msg 错误信息
*flag true:显示错误信息，false:请空错误信息
*/
function err_msg_show(inputId, spanId, msg, flag){
	if(flag){
		$("#" + spanId).text(msg);
    	$("#" + inputId).css("border-color","red");
	}else{
		$("#" + spanId).text('');
    	$("#" + inputId).css("border-color","");
	}
	
}